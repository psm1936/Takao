package ClassMethod;

import java.util.Scanner;

public class Plus민호 {

	public int plus(int num1, int num2) {		
	
		int result = num1 + num2;
	
		return result; 
		//2가지 수의 합
	}
	public int plus(int num1, int num2, int num3) {		
		
		int result = num1 + num2+ num3;
	
		return result; 
		//3가지 수의 합
	}

}
