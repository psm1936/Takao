
public class Person {
	
	private String name;
	private String job;
	private int age;
	
	
	public Person(String name, String job, int age) {
		
		this.name = name;
		this.job = job;
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public String getJob() {
		return job;
	}


	public int getAge() {
		return age;
	}
	
		
	
	
	

}
